package data.scrapping.news.vo;

public class DukasDescDetails {

	private String source;
	private String measures;
	private String usual_effect;
	private String frequency;
	private String next_release;
	private String derived_via;
	private String acro;

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getMeasures() {
		return measures;
	}

	public void setMeasures(String measures) {
		this.measures = measures;
	}

	public String getUsual_effect() {
		return usual_effect;
	}

	public void setUsual_effect(String usual_effect) {
		this.usual_effect = usual_effect;
	}

	public String getFrequency() {
		return frequency;
	}

	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}

	public String getNext_release() {
		return next_release;
	}

	public void setNext_release(String next_release) {
		this.next_release = next_release;
	}

	public String getDerived_via() {
		return derived_via;
	}

	public void setDerived_via(String derived_via) {
		this.derived_via = derived_via;
	}

	public String getAcro() {
		return acro;
	}

	public void setAcro(String acro) {
		this.acro = acro;
	}

}
